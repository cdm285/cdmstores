const state = {
    lang: localStorage.getItem("cdm_lang") || detectLang(),
    selectedProduct: null
};

function detectLang() {
    const nav = navigator.language || navigator.userLanguage;
    if (!nav) return "en";
    if (nav.toLowerCase().startsWith("es")) return "es";
    if (nav.toLowerCase().startsWith("pt")) return "pt";
    return "en";
}

function applyTranslations(lang) {
    state.lang = lang;
    localStorage.setItem("cdm_lang", lang);
    document.documentElement.lang = lang;

    // Traduzir elementos com data-i18n
    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.dataset.i18n;
        const text = translations[lang]?.[key];
        if (text) el.textContent = text;
    });

    // Hero h1: usa innerHTML para preservar <br> e <em>
    const heroH1 = document.querySelector(".hero-inner h1");
    if (heroH1) {
        const h1Map = {
            pt: "ELETR\u00d4NICOS PREMIUM,<br><em>ENTREGA GARANTIDA.</em>",
            en: "PREMIUM ELECTRONICS,<br><em>GUARANTEED DELIVERY.</em>",
            es: "ELECTR\u00d3NICOS PREMIUM,<br><em>ENTREGA GARANTIZADA.</em>"
        };
        heroH1.innerHTML = h1Map[lang] || h1Map.pt;
    }

    // Traduzir placeholders
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
        const key = el.dataset.i18nPlaceholder;
        const text = translations[lang]?.[key];
        if (text) el.setAttribute("placeholder", text);
    });

    // Traduzir cart modal
    const cartHeader = document.querySelector(".cart-modal-header h2");
    if (cartHeader) cartHeader.textContent = translations[lang]?.["cart.my"] || "🛒 Meu Carrinho";
    
    const cartEmpty = document.querySelector(".cart-empty");
    if (cartEmpty) cartEmpty.textContent = translations[lang]?.["cart.empty"] || "Carrinho vazio";

    const couponInput = document.getElementById("coupon-input");
    if (couponInput) couponInput.placeholder = translations[lang]?.["cart.coupon"] || "Código do cupom";

    const couponBtn = document.querySelector(".btn-coupon");
    if (couponBtn) couponBtn.textContent = translations[lang]?.["cart.apply"] || "Aplicar";

    // Traduzir labels do carrinho
    const cartLabels = document.querySelectorAll(".cart-total p");
    if (cartLabels.length >= 3) {
        cartLabels[0].innerHTML = `${translations[lang]?.["cart.subtotal"] || "Subtotal"}: <strong>$ <span id="cart-subtotal">0.00</span></strong>`;
        cartLabels[2].textContent = `${translations[lang]?.["cart.shipping"] || "Shipping"}: Calculated at checkout`;
    }
    const cartTotalH3 = document.querySelector(".cart-total h3");
    if (cartTotalH3) cartTotalH3.textContent = `${translations[lang]?.["cart.total"] || "Total"}: $ ${document.getElementById("cart-total-amount")?.textContent || "0.00"}`;

    // Traduzir produtos (se existirem na página)
    document.querySelectorAll(".card h3").forEach((el, idx) => {
        const prodNum = idx + 1;
        const titleKey = `products.p${prodNum}.title`;
        const text = translations[lang]?.[titleKey];
        if (text) el.textContent = text;
    });

    document.querySelectorAll(".card p").forEach((el, idx) => {
        const prodNum = Math.floor(idx / 2) + 1; // 2 p tags per card
        const descKey = `products.p${prodNum}.desc`;
        const text = translations[lang]?.[descKey];
        if (text && el.textContent.includes("$")) {
            const price = el.textContent.match(/\$[\d.]+/)?.[0] || "";
            el.innerHTML = `${price} - ${text}`;
        }
    });

    // Traduzir botões do carrinho
    document.querySelectorAll(".btn-card").forEach((btn) => {
        if (btn.textContent.includes("Adicionar") || btn.textContent.includes("Add") || btn.textContent.includes("Agregar")) {
            btn.textContent = translations[lang]?.["products.buy"] || "Adicionar ao Carrinho";
        }
    });

    // Traduzir auth modal se existir
    const loginForm = document.getElementById("login-form");
    const registerForm = document.getElementById("register-form");
    
    if (loginForm) {
        const loginTitle = loginForm.querySelector("h2");
        if (loginTitle) loginTitle.textContent = translations[lang]?.["auth.login_title"] || "Entrar";
        
        const loginBtn = loginForm.querySelector("button[type='submit']");
        if (loginBtn) loginBtn.textContent = translations[lang]?.["auth.login_button"] || "Entrar";
        
        const emailInput = loginForm.querySelector("input[type='email']");
        if (emailInput) emailInput.placeholder = translations[lang]?.["auth.email"] || "Email";
        
        const passwordInput = loginForm.querySelector("input[type='password']");
        if (passwordInput) passwordInput.placeholder = translations[lang]?.["auth.password"] || "Senha";
        
        const signupLink = loginForm.querySelector("button[type='button']");
        if (signupLink) signupLink.textContent = translations[lang]?.["auth.signup_link"] || "Não tem conta? Registre-se";
    }

    if (registerForm) {
        const registerTitle = registerForm.querySelector("h2");
        if (registerTitle) registerTitle.textContent = translations[lang]?.["auth.register_title"] || "Criar Conta";
        
        const registerBtn = registerForm.querySelector("button[type='submit']");
        if (registerBtn) registerBtn.textContent = translations[lang]?.["auth.register_button"] || "Criar Conta";
        
        const inputs = registerForm.querySelectorAll("input");
        inputs.forEach((input) => {
            if (input.type === "email") input.placeholder = translations[lang]?.["auth.email"] || "Email";
            if (input.type === "password") input.placeholder = translations[lang]?.["auth.password"] || "Senha";
            if (input.type === "text" && input.name === "name") input.placeholder = translations[lang]?.["auth.name"] || "Nome";
            if (input.type === "text" && input.name === "phone") input.placeholder = translations[lang]?.["auth.phone"] || "Telefone";
        });
        
        const loginLink = registerForm.querySelector("button[type='button']");
        if (loginLink) loginLink.textContent = translations[lang]?.["auth.login_link"] || "Já tem conta? Entrar";
    }

    // Traduzir botões OAuth
    const googleBtn = document.getElementById("google-login-btn");
    if (googleBtn) googleBtn.textContent = "🔷 " + (translations[lang]?.["auth.google"] || "Entrar com Google");
    
    const facebookBtn = document.getElementById("facebook-login-btn");
    if (facebookBtn) facebookBtn.textContent = translations[lang]?.["auth.facebook"] || "📘 Entrar com Facebook";

    // Traduzir FAB login (se existir)
    const fabLogin = document.getElementById("fab-login");
    if (fabLogin) fabLogin.title = translations[lang]?.["auth.user_login"] || "👤 Entrar";

    updateCheckoutSelection();
}

function initLanguageSwitcher() {
    const selector = ".desktop-lang-btn[data-lang], .mobile-lang-btn[data-lang]";
    const buttons = document.querySelectorAll(selector);
    buttons.forEach((btn) => {
        btn.addEventListener("click", () => {
            applyTranslations(btn.dataset.lang);
            document.querySelectorAll(selector).forEach(b => {
                b.classList.toggle("active", b.dataset.lang === btn.dataset.lang);
                b.setAttribute("aria-pressed", b.dataset.lang === btn.dataset.lang ? "true" : "false");
            });
        });
    });
    applyTranslations(state.lang);
    document.querySelectorAll(selector).forEach(b => {
        const isActive = b.dataset.lang === state.lang;
        b.classList.toggle("active", isActive);
        b.setAttribute("aria-pressed", isActive ? "true" : "false");
    });
}

function initMobileNav() {
    const hamburger = document.getElementById("hamburger-btn");
    const nav = document.getElementById("mobile-nav");
    const overlay = document.getElementById("mobile-nav-overlay");
    const closeBtn = document.getElementById("mobile-nav-close");
    if (!hamburger || !nav || !overlay) return;

    function openNav() {
        hamburger.classList.add("is-open");
        nav.classList.add("is-open");
        overlay.classList.add("is-open");
        hamburger.setAttribute("aria-expanded", "true");
        nav.setAttribute("aria-hidden", "false");
        document.body.classList.add("nav-open");
    }

    function closeNav() {
        hamburger.classList.remove("is-open");
        nav.classList.remove("is-open");
        overlay.classList.remove("is-open");
        hamburger.setAttribute("aria-expanded", "false");
        nav.setAttribute("aria-hidden", "true");
        document.body.classList.remove("nav-open");
    }

    // Use both click and touchend to ensure reliability on all mobile browsers
    function handleHamburgerActivate(e) {
        e.preventDefault();
        openNav();
    }

    hamburger.addEventListener("click", openNav);
    // touchend with preventDefault prevents the 300ms ghost-click delay
    hamburger.addEventListener("touchend", handleHamburgerActivate, { passive: false });

    overlay.addEventListener("click", closeNav);
    overlay.addEventListener("touchend", (e) => { e.preventDefault(); closeNav(); }, { passive: false });
    if (closeBtn) {
        closeBtn.addEventListener("click", closeNav);
        closeBtn.addEventListener("touchend", (e) => { e.preventDefault(); closeNav(); }, { passive: false });
    }

    // Close when any nav link is tapped
    nav.querySelectorAll(".mobile-nav-link, .mobile-nav-cta").forEach(el => {
        el.addEventListener("click", closeNav);
    });

    // Close on Escape key
    document.addEventListener("keydown", (e) => {
        if (e.key === "Escape" && nav.classList.contains("is-open")) closeNav();
    });
}

function initHeaderScroll() {
    const header = document.querySelector(".header");
    if (!header) return;
    window.addEventListener("scroll", () => {
        if (window.scrollY > 80) header.classList.add("scrolled");
        else header.classList.remove("scrolled");
    });
}

function initProductButtons() {
    document.querySelectorAll(".btn-card[data-product]").forEach((btn) => {
        btn.addEventListener("click", () => {
            const id = btn.dataset.product;
            const product = products[id];
            if (!product) return;
            localStorage.setItem("cdm_selected_product", JSON.stringify({ id }));
            const target = location.pathname.includes("pages/") ? "checkout.html" : "pages/checkout.html";
            window.location.href = target;
        });
    });
}

function updateCheckoutSelection() {
    const selectionEl = document.getElementById("checkout-selected");
    if (!selectionEl) return;
    const stored = localStorage.getItem("cdm_selected_product");
    const parsed = stored ? JSON.parse(stored) : null;
    const id = parsed?.id || "p1";
    const product = products[id] || products.p1;
    const nameKey = `products.${id}.title`;
    const name = translations[state.lang]?.[nameKey] || translations[state.lang]?.["products.p1.title"];
    const selectionLabel = translations[state.lang]?.["checkout.selection"] || "Produto";
    const price = product.price?.toLocaleString('en-US', { style: "currency", currency: "USD" }) || "";
    selectionEl.textContent = `${selectionLabel} ${name} ${price ? `(${price})` : ""}`;
}

function initCheckout() {
    const btnStripe = document.getElementById("btn-stripe");
    if (!btnStripe) return;
    const stored = localStorage.getItem("cdm_selected_product");
    const parsed = stored ? JSON.parse(stored) : null;
    const id = parsed?.id || "p1";
    btnStripe.addEventListener("click", () => {
        const product = products[id] || products.p1;
        const link = product.stripeLink || products.p1.stripeLink;
        window.open(link, "_blank", "noopener");
    });
}

function renderTrackingResult(code, record) {
    const box = document.getElementById("resultado-rastreio");
    if (!box) return;
    box.classList.add("visible");
    const lang = state.lang;
    if (!record) {
        box.innerHTML = `<p><strong>${translations[lang]?.["tracking.notFound"] || "Código não encontrado."}</strong></p><p>${translations[lang]?.["tracking.help"] || "Revise o código ou fale com nosso atendimento."}</p>`;
        return;
    }
    const statusLabel = translations[lang]?.["tracking.status"] || "Status:";
    const etaLabel = translations[lang]?.["tracking.eta"] || "Previsão:";
    const foundLabel = translations[lang]?.["tracking.found"] || "Código localizado.";
    const statusText = record.status?.[lang] || record.status?.pt || "-";
    box.innerHTML = `
        <p><strong>✓ ${foundLabel}</strong></p>
        <p>${statusLabel} <strong>${statusText}</strong></p>
        <p>${etaLabel} <strong>${record.eta || "-"}</strong></p>
        <p style="color:var(--text-muted);font-size:0.82rem;">Última atualização: ${record.lastUpdate || "-"}</p>
    `;
}

function initTracking() {
    const button = document.getElementById("btn-rastrear");
    const input = document.getElementById("codigo-rastreio");
    if (!button || !input) return;
    const handler = () => {
        const code = input.value.trim().toUpperCase();
        const record = trackingDB[code];
        renderTrackingResult(code, record);
    };
    button.addEventListener("click", handler);
    input.addEventListener("keypress", (e) => {
        if (e.key === "Enter") handler();
    });
}

